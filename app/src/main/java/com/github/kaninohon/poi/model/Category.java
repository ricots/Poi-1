package com.github.kaninohon.poi.model;

public class Category {
    private String label;

    private String id;

    public void setLabel(String label){
        this.label = label;
    }
    public String getLabel(){
        return this.label;
    }
    public void setId(String id){
        this.id = id;
    }
    public String getId(){
        return this.id;
    }

}
